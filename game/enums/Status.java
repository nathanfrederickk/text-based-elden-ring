package game.enums;

/**
 * Use this enum class to give `buff` or `debuff`.
 * Created by:
 * @author Riordan D. Alfredo
 */
public enum Status {

    /**
     * If the Actor is hostile to enemy
     */
    HOSTILE_TO_ENEMY,

    /**
     * If the Actor is hostile to player
     */
    HOSTILE_TO_PLAYER,

    /**
     * If the Actor can revive into another actor
     */
    REVIVABLE,

    /**
     * If the Actor is a player
     */
    PLAYER
}
