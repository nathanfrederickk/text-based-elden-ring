/**
 * To represent the Job that the Player can choose
 * before start playing the game
 */
public enum Job {

    /**
     * If player chooses Samurai class
     */
    SAMURAI,

    /**
     * If player chooses Bandit class
     */
    BANDIT,

    ASTROLOGER,
    /**
     * If player chooses Wretch class
     */
    WRETCH
}
